function m_inits(main,sub,defaultmain,defaultsub)
{
	var index = 0;
	for(var i = 0;i < m_maintype.length;i ++)
	{
		var option = new Option(m_maintype[i],m_maintype[i]);
		main.add(option,i);
		if(defaultmain == m_maintype[i])
		{
			main.selectedIndex = i;
			index = i;
		}
	}
	for(i = 0;i < m_subtype[index].length;i ++)	
	{
		option = new Option(m_subtype[index][i],m_subtype[index][i]);
		sub.add(option,i);
		if(defaultsub == m_subtype[index][i])
			sub.selectedIndex = i;
	}
	
}
function m_change(main,sub)
{
	var index = main.selectedIndex;
	sub.length = 0;
	for(var i = 0;i < m_subtype[index].length;i ++)	
	{
		var option = new Option(m_subtype[index][i],m_subtype[index][i]);
		sub.add(option,i);
	}
}